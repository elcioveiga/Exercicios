
 function modoClaro() {
    document.body.style.backgroundColor = "lightcoral";
    document.body.style.color = "darkred";
    
    document.getElementById("mensagem").innerText ="Modo claro ativado!";
}


function modoEscuro() {
    document.body.style.backgroundColor = "darkred";
    document.body.style.color = "lightcoral";
    
    document.getElementById("mensagem").innerText ="Modo escuro ativado!";
}